package com.food.pojo;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Admin
 */
@Entity
@Table(name = "statusorder")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Statusorder.findAll", query = "SELECT s FROM Statusorder s"),
    @NamedQuery(name = "Statusorder.findById", query = "SELECT s FROM Statusorder s WHERE s.id = :id"),
    @NamedQuery(name = "Statusorder.findByName", query = "SELECT s FROM Statusorder s WHERE s.name = :name")})
public class Statusorder implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 20)
    @Column(name = "name")
    private String name;
    @OneToMany(mappedBy = "idStatusorder")
    private Collection<Orderfood> orderfoodCollection;

    public Statusorder() {
    }

    public Statusorder(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @XmlTransient
    public Collection<Orderfood> getOrderfoodCollection() {
        return orderfoodCollection;
    }

    public void setOrderfoodCollection(Collection<Orderfood> orderfoodCollection) {
        this.orderfoodCollection = orderfoodCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Statusorder)) {
            return false;
        }
        Statusorder other = (Statusorder) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.food.pojo.Statusorder[ id=" + id + " ]";
    }
    
}
